/**
 * 获取所有模板列表
 */
export function getAllTemplates() {
  return Object.entries(quickTemplates).map(([key, value]) => ({
    id: key,
    ...value
  }))
}

/**
 * 根据领域获取相关模板
 */
export function getTemplatesByDomain(domain) {
  return Object.entries(quickTemplates)
    .filter(([key, template]) => template.domain === domain)
    .map(([key, value]) => ({
      id: key,
      ...value
    }))
}
